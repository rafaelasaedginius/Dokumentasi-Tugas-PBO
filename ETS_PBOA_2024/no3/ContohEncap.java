public class ContohEncap {
    private String nama;
    private int nilai;
    private String matkul;

    public String getNamaSekarang() {
        return this.nama;
    }

    public void setNamamu(String namamu) {
        this.nama = namamu;
    }

    public int getNilaimu() {
        return this.nilai;
    }

    public void setNilaimu(int nilai) {
        this.nilai = nilai;
    }

    public String getMatkul() {
        return this.matkul;
    }

    public void setMatkul(String matkul) {
        this.matkul = matkul;
    }
}
